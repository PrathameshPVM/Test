//WAP to check if it is vowel
#include <stdio.h>
void main()
{
    char ch;
    printf("Enter any character to check if it is vowel");
    scanf("%c",&ch);

    switch (ch)
    {
    case 'a':
        printf("Entered character is vowel");
        break;
    case 'e':
        printf("Entered character is vowel");
        break;
    case 'i':
        printf("Entered character is vowel");
        break;    
    case 'o':
        printf("Entered character is vowel");
        break;
    case 'u':
        printf("Entered character is vowel");
        break;
    default:
        printf("Entered character is vowel");
        break;
    }
    
}